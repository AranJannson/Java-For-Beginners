/**
 * An interface for a market.
 * An interface is a contract that a class can implement.
 * It is a way to standardize the way a class is implemented.
 *
 * <p>You can think of it like the skeleton of a class
 */
public interface Market {
  void startShoppingSession(); // Method to start a shopping session in the market

  void endShoppingSession(); // Method to end a shopping session in the market
}
